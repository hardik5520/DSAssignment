package com.assignment.dsassignment;

public class Value {
	
	private float val1;
	private float val2;
	public Value(float val1, float val2) {
		super();
		this.val1 = val1;
		this.val2 = val2;
	}
	public float getVal1() {
		return val1;
	}
	public void setVal1(int val1) {
		this.val1 = val1;
	}
	public float getVal2() {
		return val2;
	}
	public void setVal2(int val2) {
		this.val2 = val2;
	}
	
	

}
